//while loop 
let num=20;
while(num>0)
{
   console.log(num);
   num--; 
}
console.log(" while Loop end");

//for loop
for (let i = 1; i <= 5; i++) {
    console.log(i);
}
console.log("  for Loop end");

//do-while loop 
let i = 1;

do {
    console.log(i);
    i++;
} while (i <= 5);
console.log(" do-while Loop end");

